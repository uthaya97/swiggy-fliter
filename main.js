function itemRender(inpObj) {
    // console.log(inpObj);

    inpObj.forEach(function (e) {

        let col = document.createElement("div")
        col.classList.add("fliter-col");
        col.classList.add(e.categoroy);
        row.appendChild(col)

        let card = document.createElement("div")
        card.classList.add("fliter-card");
        col.appendChild(card)

        let cardImg = document.createElement("div")
        cardImg.classList.add("fliter-img");
        card.appendChild(cardImg)

        let img = document.createElement("img")
        img.setAttribute("src", e.imgUrl)
        cardImg.appendChild(img)

        let cardCon = document.createElement("div")
        cardCon.classList.add("fliter-content");
        card.appendChild(cardCon)

        let heading = document.createElement("h1")
        heading.classList.add("fliter-heading");
        cardCon.appendChild(heading)
        heading.innerHTML = e.name

        let flIcon = document.createElement("div")
        flIcon.classList.add("fliter-icons");
        cardCon.appendChild(flIcon)

        let icon = document.createElement("i")
        icon.classList.add("fa")
        icon.classList.add("fa-star")
        icon.setAttribute("aria-hidden", true)
        flIcon.appendChild(icon)

        let para = document.createElement("p")
        para.classList.add("fliter-para");
        flIcon.appendChild(para)
        para.innerHTML = e.rating

        let para1 = document.createElement("p")
        para1.classList.add("fliter-para1");
        cardCon.appendChild(para1)
        para1.innerHTML = e.cuisine

        let price = document.createElement("p")
        price.classList.add("fliter-price");
        cardCon.appendChild(price)
        price.innerHTML = "Rs. " + e.preparetion

        let units = document.createElement("p")
        units.classList.add("fliter-units");
        cardCon.appendChild(units)
        units.innerHTML = e.units

        let units1 = document.createElement("p")
        units1.classList.add("fliter-units1");
        cardCon.appendChild(units1)
        units1.innerHTML = "Bucket - " + "Rs. " + e.amount

        let units2 = document.createElement("p")
        units2.classList.add("fliter-units2");
        cardCon.appendChild(units2)
        units2.innerHTML = "Delivery By " + e.delivery + "  minutes"





        if (e.rating >= 4.4) {
            icon.style.backgroundColor = "red"
            icon.style.color = "white"
        }
        else if (e.rating >= 4 && e.rating < 4.4) {
            icon.style.backgroundColor = "green"
            icon.style.color = "white"
        }
        else if (e.rating < 4 && e.rating >= 3.5) {
            icon.style.backgroundColor = "yellow"
            icon.style.color = "white"
        }
        else {
            icon.style.backgroundColor = "blue"
            icon.style.color = "white"
        }

    })

}

